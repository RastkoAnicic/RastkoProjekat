    private void postaviDatumIBrojRacuna() {

        DateFormat sdf = new SimpleDateFormat("dd.MM.yyyy");
        Date danasnji = new Date();
        labela_datum.setText(sdf.format(danasnji) + ".");

        try {
            LinkedList<Racun> ra = kontrolor.Kontrolor.getInstance().vratiListuRacuna();
            labela_br_racuna.setText(Integer.toString(ra.size() + 1));
        } catch (Exception ex) {
//            JOptionPane.showMessageDialog(this, "Ne mogu da vratim racune");
            ex.printStackTrace();
        }

    }

    private void postaviKlijenta(String klijent) {
        labela_klijent.setText(klijent);
    }

    private void popuniComboe() {

        try {
            List<Aranzman> aranzmani = kontrolor.Kontrolor.getInstance().vratiListuAranzmanaPravih();
            List<TipAranzmana> tipovi = kontrolor.Kontrolor.getInstance().vratiListuAranzmana();
            jcmb_aranzman.setModel(new DefaultComboBoxModel(aranzmani.toArray()));
            jcmb_tipAranzmana.setModel(new DefaultComboBoxModel(tipovi.toArray()));
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "Ne mogu da popunim comboe");
        }

    }

    private void postaviTabelu() {
     
        TableModelStavkeRacuna tsr = new TableModelStavkeRacuna();
        jtbl_stavke_racuna.setModel(tsr);
        
    }
