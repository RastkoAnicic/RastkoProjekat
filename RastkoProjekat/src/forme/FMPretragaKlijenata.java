    private void popuniTabelu() {

        try {
            List<Klijent> klijenti = Kontrolor.getInstance().vratiListuKlijenata();
            TableModelKlijenti tbl = new TableModelKlijenti(klijenti);
            jtbl_klijenti.getTableHeader().setReorderingAllowed(false);
            jtbl_klijenti.setModel(tbl);
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage());
        }
    }
    
    private void documentListenerPretraga(){
        DocumentListener dl = new DocumentListener() {
            @Override
            public void insertUpdate(DocumentEvent e) {
               pretrazi();
            }

            @Override
            public void removeUpdate(DocumentEvent e) {
               pretrazi();
            }

            @Override
            public void changedUpdate(DocumentEvent e) {
              pretrazi();
            }

        };
        jtxt_pretrazi.getDocument().addDocumentListener(dl);
    }
            private void pretrazi() {
                
                String pretraga = jtxt_pretrazi.getText().toLowerCase().trim();
                
                List<Klijent> klijenti = new LinkedList<>();
                
        try {
            List<Klijent> klijentiSVI = Kontrolor.getInstance().vratiListuKlijenata();
            for (Klijent klijent : klijentiSVI) {
                if(klijent.getIme().toLowerCase().contains(pretraga) ||
                        klijent.getPrezime().toLowerCase().contains(pretraga) || klijent.getMejl().contains(pretraga) ||
                            klijent.getTelefon().contains(pretraga) || klijent.getJmbg().contains(pretraga)){
                    klijenti.add(klijent);
                }
            }
            TableModelKlijenti tbl = new TableModelKlijenti(klijenti);
            jtbl_klijenti.setModel(tbl);
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "Greska");
        }
                
            }
